def get_munger_content(day):
    """获取芒格内容"""
    day = (day - 1) % len(MUNGER_CONTENT) + 1
    return MUNGER_CONTENT.get(day, MUNGER_CONTENT[1])

def get_time_greeting():
    """根据时间返回问候语"""
    hour = datetime.now().hour
    if 6 <= hour < 12:
        return "🌅 早安！"
    elif 12 <= hour < 18:
        return "🌞 午安！"
    else:
        return "🌙 晚安！"

def format_message():
    """格式化推送消息（一次推送多条）"""
    global DAY
    global PUSH_COUNT
    
    greeting = get_time_greeting()
    title = f"【{greeting}芒格智慧合集 · 共{PUSH_COUNT}条】"
    
    desp = ""
    for i in range(PUSH_COUNT):
        content = get_munger_content(DAY)
        
        desp += f"""
---
## Day{DAY} · {content['title']}
{content['content']}

📖 **芒格原话**："{content['quote']}"
"""
        
        DAY += 1
    
    desp += f"""
---
🕐 推送时间: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}
💪 每日进步一点点，30天完成蜕变
"""
    
    return title, desp

def push_to_wechat(title, desp):
    """推送到微信"""
    url = f"https://sctapi.ftqq.com/{SENDKEY}.send"
    data = {"title": title, "desp": desp}
    
    try:
        response = requests.post(url, data=data)
        result = response.json()
        if result.get("code") == 0 or result.get("errno") == 0:
            print("✅ 推送成功！请查看微信消息")
            return True
        else:
            print(f"❌ 推送失败: {result.get('message', '未知错误')}")
            return False
    except Exception as e:
        print(f"❌ 推送异常: {e}")
        return False

def main():
    print("=" * 50)
    print("        芒格智慧每日推送 v1.0")
