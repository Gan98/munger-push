def build_message(day: int, count: int) -> tuple:
    """
    构建推送消息（365天不重复版）。

    参数：
        day: 起始天数
        count: 推送几条

    返回：(标题, 内容, 结束时的新day值)
    """
    greeting = get_time_greeting()
    title = f"【{greeting}芒格智慧 · 共{count}条】"

    parts = []
    current = day

    for _ in range(count):
        # 365天不重复算法：循环使用基础模型，但搭配不同的练习题和行动建议
        model_idx = (current - 1) % CONTENT_SIZE + 1
        exercise_idx = (current - 1) % len(EXERCISES)
        action_idx = (current - 1) % len(ACTIONS)
        
        c = CONTENT[model_idx]
        
        # 动态生成内容
        content = f"""---
## 📚 Day{current} · {c['title']}

### 🔑 核心原理
{c['core']}

### 🌐 跨学科视角
{chr(10).join(c['perspectives'])}

### 💬 生活场景
{c['case']}

### 🎯 今日行动
{ACTIONS[action_idx]}

### 🤔 深度思考
{c['question']}

### 📖 芒格原话
"{c['quote']}"

### 📝 练习题
{EXERCISES[exercise_idx]}
"""
        parts.append(content)
        current += 1
        
        # 达到最大天数后重置
        if current > MAX_DAYS:
            current = 1

    footer = f"""
---
🕐 推送时间: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}
💪 每日进步一点点，持续蜕变进行中...
📅 第 {current - 1} 天 / 共 {MAX_DAYS} 天
"""
    return title, "".join(parts) + footer, current


